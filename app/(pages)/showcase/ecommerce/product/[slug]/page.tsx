type ProductResultType = {
  ok: boolean;
  data: Product;
  error: string;
};
type ProductsType = {
  ok: boolean;
  data: Product[];
};

export async function generateStaticParams() {
  const products: ProductsType = await getAllProducts();

  return products?.data?.map((product) => ({
    slug: product?.id,
  }));
}

